#include <bits/stdc++.h>
#define Max 100022
using namespace std;
double num[Max];
int n;
int main(){
    cin >> n;
    for(int i = 0; i < n; i++){
        cin >> num[i];
    }
    double sum = 0, ans = 0;
    // for(int i = 0; i < n; i++){
    //     sum = num[i];
    //     ans += sum;
    //     for(int j = i + 1; j < n; j++) {
    //         sum += num[j];
    //         ans += sum;
    //     }
    // }
    for(int i = 0; i < n; i++){
        ans += num[i]*(n-i)*(i+1);
    }
    cout << fixed << setprecision(2) << ans << endl;
    return 0;
}
