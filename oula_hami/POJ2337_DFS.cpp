/*
连通+存在欧拉路(每个节点入度=出度 或者 除两个节点(入度-出度=-1 入度-出度=1)外，其他节点入度=出度)

如果满足 in-out == -1 的话 就一定是顶点
因为这道题需要输出字典序最小的，那么得先给他排序，按照字典序从小到大排，这样就能保证在 条件都满足的情况下 的输出顺序
*/
#include <iostream>
#include <cstring>
#include <string>
#include <algorithm>
#include <queue>
#include <stack>
#define ll long long
#define inf 0x3f3f3f3f
#define Max 1020
using namespace std;
string str[Max];
int vis[Max],ans[Max];
int in[30],out[30],head[30];
int t,n,u,v,cnt,tot,st;
struct Node
{
    int to,nex;
    int index;
}edge[Max];
void init()
{
    cnt=0,tot=0;
    memset(in,0,sizeof(in));
    memset(out,0,sizeof(out));
    memset(head,-1,sizeof(head));
    memset(ans,0,sizeof(ans));
    memset(vis,0,sizeof(vis));
}
//注意是反向建边
void add_edge(int u,int v,int index)
{
    edge[tot].to=v;
    edge[tot].nex=head[u];
    edge[tot].index=index;
    head[u]=tot++;
}
//dfs  欧拉回路输出
void dfs(int u)
{
    for(int v=head[u];~v;v=edge[v].nex)
    {
        if(!vis[v])
        {
            vis[v]=1;
            dfs(edge[v].to);
            ans[cnt++]=edge[v].index;
        }
    }
}

int main()
{
    ios::sync_with_stdio(false);
    cin>>t;
    while(t--)
    {
        init();
        cin>>n;
        for(int i=0;i<n;i++)
            cin>>str[i];
        sort(str,str+n);
        st=inf;
        //这里是反向存储 则反反得正 
        for(int i=n-1;i>=0;i--)
        {
            int len=str[i].length();
            u=str[i][0]-'a',v=str[i][len-1]-'a';
            in[v]++,out[u]++;
            add_edge(u,v,i);
            st=min(u,v);//针对欧拉回路
        }
        int cnt1=0,cnt2=0,num=0;
        for(int i=0;i<26;i++)
        {
            if(!in[i]&&!out[i]) continue;
            if(in[i]!=out[i]) num++;//统计入度!=出度的点的个数 用于回路和路径的区别
            if(in[i]-out[i]==1) //进-出-> 1  终点
            { cnt1++;}
            else if(in[i]-out[i]==-1) // 进-出-> -1 起点
            { cnt2++; st=i; }
        }
        //st 必须只改变一次 且为最小
        if(num>0)
        {
            //不是欧拉回路 也不是 欧拉路径
            if(!(cnt1==1&&cnt2==1&&num==2))
            { cout<<"***"<<endl; continue; }
        }
        cnt=0;
        dfs(st);
        if(cnt!=n)//说明不连通
        { cout<<"***"<<endl;  continue;  }
        //因为深搜 回溯 的原因 最先存的是最后的单词 所以是 i=cnt-1
        for(int i=cnt-1;i>=0;i--)
        {
            cout<<str[ans[i]];
            if(i!=0) cout<<".";
            else cout<<endl;
        }
    }
    return 0;
}
